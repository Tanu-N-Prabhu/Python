commands = ["LRUCache", "put", "put", "get", "put", "get", "put", "get", "get", "get"]
values = [[2], [1, 1], [2, 2], [1], [3, 3], [2], [4, 4], [1], [3], [4]]

cache = None
results = []

for i in range(len(commands)):
    command = commands[i]
    value = values[i]
    if command == "LRUCache":
        cache = LRUCache(value[0])
        results.append(None)
    elif command == "get":
        result = cache.get(value[0])
        results.append(result)
    elif command == "put":
        cache.put(value[0], value[1])
        results.append(None)

print(results)

